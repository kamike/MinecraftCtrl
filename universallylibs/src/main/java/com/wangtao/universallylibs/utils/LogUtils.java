package com.wangtao.universallylibs.utils;

import android.util.Log;

public class LogUtils {
    public static void logFile(Object obj) {
        Log.i("log_file", obj + "");
    }

    public static void logMap(Object string) {
        Log.i("log_map", string + "");
    }

    public static void logView(String string) {
        Log.i("log_view", string + "");
    }

    public static void logNetwork(String string) {
        Log.i("log_network", string + "");
    }


    public static void logTime(String string) {
        Log.i("log_time", string + "");
    }



    public static void logImg(String uri) {
        Log.i("log_img", uri);

    }


}
